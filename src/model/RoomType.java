package model;

public enum RoomType {
    /**
     * A room in the top left-hand corner of the maze.
     */
    TOP_LEFT,
    /**
     * A room against the top wall of the maze.
     */
    TOP,
    /**
     * A room in the top right-hand corner of the maze.
     */
    TOP_RIGHT,
    /**
     * A room against the left wall of the maze.
     */
    LEFT,
    /**
     * An interior room in the maze.
     */
    INTERIOR,
    /**
     * A room against the right wall of the maze.
     */
    RIGHT,
    /**
     * A room in the bottom left-hand corner of the maze.
     */
    BOT_LEFT,
    /**
     * A room against the bottom wall of the maze.
     */
    BOT,
    /**
     * A room in the bottom right-hand corner of the maze.
     */
    BOT_RIGHT
}
